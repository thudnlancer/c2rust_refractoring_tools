/// Constant-time memory comparison
/// 
/// Returns true if the two memory regions are equal, false otherwise.
/// The comparison is done in constant time to prevent timing attacks.
pub fn memeql_sec(a: &[u8], b: &[u8]) -> bool {
    if a.len() != b.len() {
        return false;
    }

    let mut diff = 0u8;
    
    for (ai, bi) in a.iter().zip(b.iter()) {
        diff |= ai ^ bi;
    }

    diff == 0
}