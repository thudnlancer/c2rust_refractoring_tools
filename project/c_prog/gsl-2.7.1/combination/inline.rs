#[derive(Copy, Clone)]
#[repr(C)]
pub struct gsl_combination_struct {
    pub n: size_t,
    pub k: size_t,
    pub data: *mut size_t,
}
pub type gsl_combination = gsl_combination_struct;
#[no_mangle]
pub unsafe extern "C" fn gsl_combination_get(
    mut c: *const gsl_combination,
    i: size_t,
) -> size_t {
    if gsl_check_range != 0 && i >= (*c).k {
        gsl_error(
            b"index out of range\0" as *const u8 as *const libc::c_char,
            b"../gsl/gsl_combination.h\0" as *const u8 as *const libc::c_char,
            82 as libc::c_int,
            GSL_EINVAL as libc::c_int,
        );
        return 0 as libc::c_int as size_t;
    }
    return *((*c).data).offset(i as isize);
}
