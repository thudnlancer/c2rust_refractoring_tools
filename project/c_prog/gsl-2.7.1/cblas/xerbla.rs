#[no_mangle]
pub unsafe extern "C" fn cblas_xerbla(
    mut p: libc::c_int,
    mut rout: *const libc::c_char,
    mut form: *const libc::c_char,
    mut args: ...
) {
    let mut ap: ::core::ffi::VaListImpl;
    ap = args.clone();
    if p != 0 {
        fprintf(
            stderr,
            b"Parameter %d to routine %s was incorrect\n\0" as *const u8
                as *const libc::c_char,
            p,
            rout,
        );
    }
    vfprintf(stderr, form, ap.as_va_list());
    abort();
}
